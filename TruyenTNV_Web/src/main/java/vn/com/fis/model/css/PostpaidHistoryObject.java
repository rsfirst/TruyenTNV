package vn.com.fis.model.css;

public class PostpaidHistoryObject {
	private String action;
	private String staffName;
	private String shopCode;
	private String actionDatetime;
	private String description;
	private String logTransId;

	public String getAction() {
		return action;
	}

	public void setAction(String action) {
		this.action = action;
	}

	public String getStaffName() {
		return staffName;
	}

	public void setStaffName(String staffName) {
		this.staffName = staffName;
	}

	public String getShopCode() {
		return shopCode;
	}

	public void setShopCode(String shopCode) {
		this.shopCode = shopCode;
	}

	public String getActionDatetime() {
		return actionDatetime;
	}

	public void setActionDatetime(String actionDatetime) {
		this.actionDatetime = actionDatetime;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getLogTransId() {
		return logTransId;
	}

	public void setLogTransId(String logTransId) {
		this.logTransId = logTransId;
	}

}
