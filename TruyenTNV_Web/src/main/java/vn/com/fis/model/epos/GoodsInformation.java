package vn.com.fis.model.epos;

public class GoodsInformation
{
	private String inputWarrantyId;
	private String goods_code;
	private String name;
	private String unit;
	private String war_period;
	private String goods_warranty;
	private String goods_description;

	public String getInputWarrantyId()
	{
		return inputWarrantyId;
	}

	public void setInputWarrantyId(String inputWarrantyId)
	{
		this.inputWarrantyId = inputWarrantyId;
	}

	public String getGoods_code()
	{
		return goods_code;
	}

	public void setGoods_code(String goods_code)
	{
		this.goods_code = goods_code;
	}

	public String getName()
	{
		return name;
	}

	public void setName(String name)
	{
		this.name = name;
	}

	public String getUnit()
	{
		return unit;
	}

	public void setUnit(String unit)
	{
		this.unit = unit;
	}

	public String getWar_period()
	{
		return war_period;
	}

	public void setWar_period(String war_period)
	{
		this.war_period = war_period;
	}

	public String getGoods_warranty()
	{
		return goods_warranty;
	}

	public void setGoods_warranty(String goods_warranty)
	{
		this.goods_warranty = goods_warranty;
	}

	public String getGoods_description()
	{
		return goods_description;
	}

	public void setGoods_description(String goods_description)
	{
		this.goods_description = goods_description;
	}

}
