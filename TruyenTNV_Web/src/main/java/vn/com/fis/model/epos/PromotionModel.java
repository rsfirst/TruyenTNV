package vn.com.fis.model.epos;

public class PromotionModel {

	private String promProgramCode;
	private String name;
	private String promotionId;
	private String promAmount;
	private String promType;
	private String display;

	public String getPromProgramCode() {
		return promProgramCode;
	}

	public void setPromProgramCode(String promProgramCode) {
		this.promProgramCode = promProgramCode;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPromotionId() {
		return promotionId;
	}

	public void setPromotionId(String promotionId) {
		this.promotionId = promotionId;
	}

	public String getPromAmount() {
		return promAmount;
	}

	public void setPromAmount(String promAmount) {
		this.promAmount = promAmount;
	}

	public String getPromType() {
		return promType;
	}

	public void setPromType(String promType) {
		this.promType = promType;
	}

	public String getDisplay() {
		return display;
	}

	public void setDisplay(String display) {
		this.display = display;
	}

}
