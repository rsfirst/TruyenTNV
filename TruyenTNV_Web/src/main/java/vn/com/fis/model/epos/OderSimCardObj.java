package vn.com.fis.model.epos;

public class OderSimCardObj
{
	private int stt;
	private String serial;
	private String status;
	private String reason;
	private String checked;

	/**
	 * @return the stt
	 */
	public int getStt()
	{
		return stt;
	}

	/**
	 * @param stt
	 *            the stt to set
	 */
	public void setStt(int stt)
	{
		this.stt = stt;
	}

	/**
	 * @return the serial
	 */
	public String getSerial()
	{
		return serial;
	}

	/**
	 * @param serial
	 *            the serial to set
	 */
	public void setSerial(String serial)
	{
		this.serial = serial;
	}

	/**
	 * @return the status
	 */
	public String getStatus()
	{
		return status;
	}

	/**
	 * @param status
	 *            the status to set
	 */
	public void setStatus(String status)
	{
		this.status = status;
	}

	/**
	 * @return the reason
	 */
	public String getReason()
	{
		return reason;
	}

	/**
	 * @param reason
	 *            the reason to set
	 */
	public void setReason(String reason)
	{
		this.reason = reason;
	}

	/**
	 * @return the checked
	 */
	public String getChecked()
	{
		return checked;
	}

	/**
	 * @param checked
	 *            the checked to set
	 */
	public void setChecked(String checked)
	{
		this.checked = checked;
	}

}
